-- AI ペルソナ（運営ちゃん 68 / マダムK / オジマ）がシード投稿の本文先頭に入れていた
-- 自己紹介の挨拶文を一括で削除する。
--
-- 対象、threads.body の先頭に以下のいずれかが入っているレコード
--   「運営ちゃん（68）です。」
--   「マダムKです。」
--   「オジマです。」
--
-- これらの行と、その直後の改行・空白をまとめて取り除く。
-- 全角括弧と半角括弧の両方に対応するため文字クラスで吸収する。

update public.threads
set body = regexp_replace(
  body,
  '^(運営ちゃん[（(]68[）)]です。?|マダムKです。?|オジマです。?)[[:space:]]*',
  ''
)
where body ~ '^(運営ちゃん[（(]68[）)]です。?|マダムKです。?|オジマです。?)';

-- 返信側（replies.body）は対象外。スレッド本文のみ。
-- もし返信にも同じ自己紹介文が入っていた場合は、別途対応する。

-- 念のため、更新後に空行で始まらないか確認するために trim を追加適用
update public.threads
set body = regexp_replace(body, '^[[:space:]]+', '')
where body ~ '^[[:space:]]';
